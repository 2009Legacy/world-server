package com.rs.game.player.content.world.doors;

public class DoubleDoor {
	
	private int first;
	private int second;
	
	public DoubleDoor(int first, int second) {
		this.first = first;
		this.second = second;
	}
	
	public int getFirst() {
		return first;
	}

	public int getSecond() {
		return second;
	}
}
