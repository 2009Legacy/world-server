package com.rs.utils.music;

public class Song {
	private String name;
	private String hint;
	private int id;
	private int[] regionIds;
	
	public String getName() {
		return name;
	}
	
	public String getHint() {
		return hint;
	}
	
	public int getId() {
		return id;
	}
	
	public int[] getRegionIds() {
		return regionIds;
	}
}
